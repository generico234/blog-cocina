        Pollo con pesto<br>
        Milanesas al Horno<br><br>
        Ingredientes:<br>
        4 filetes de carne (puede ser ternera, cerdo o pollo)<br>
        1 taza de pan rallado<br>
        1/2 taza de queso parmesano rallado (opcional)<br>
        2 huevos<br>
        1/2 taza de harina<br>
        2 dientes de ajo, picados finamente<br>
        1 cucharadita de perejil fresco picado (o seco)<br>
        Sal y pimienta al gusto<br>
        Aceite de oliva (opcional)<br>
        Instrucciones:<br><br>
        Preparar los ingredientes:<br><br>

        Precalienta el horno a 200 °C (390 °F).<br>
        Aplana los filetes de carne con un mazo de cocina hasta que tengan un grosor uniforme (aproximadamente 1 cm).<br>
        Esto ayudará a que se cocinen de manera uniforme.<br>
        Sazonar la carne:<br>

        Sazona los filetes con sal, pimienta, ajo picado y perejil. Deja reposar durante unos 10-15 minutos para que
        absorban el sabor.<br>
        Preparar el empanizado:<br><br>

        En tres platos hondo, coloca la harina en uno, los huevos batidos en el segundo y mezcla el pan rallado con el
        queso parmesano en el tercero.<br>
        Empanizar la carne:<br><br>

        Pasa cada filete primero por la harina, asegurándote de cubrir bien ambos lados. Sacude el exceso.<br>
        Luego, sumérgelos en el huevo batido, dejando que el exceso escurra.<br>
        Finalmente, cúbrelos con la mezcla de pan rallado y queso, presionando un poco para que se adhiera bien.<br>
        Hornea:<br><br>

        Coloca las milanesas en una bandeja para hornear forrada con papel pergamino. Si deseas, puedes rociarlas
        ligeramente con aceite de oliva para mejorar el dorado.<br>
        Hornea durante 20-25 minutos, dándoles la vuelta a la mitad del tiempo, hasta que estén doradas y crujientes.<br>
        Servir:<br><br>

        Retira las milanesas del horno y déjalas reposar unos minutos. Sirve calientes con una guarnición de ensalada,<br>
        puré de papas o lo que prefieras.<br>
        Zapallo relleno<br>
        Empanadas:<br><br>
        <img id="empanadasdos" src="imagenes/empanadasdecarne.png"><br>
        (De Carne)<br>
        Ingredientes<br>

        Para la masa:<br>
        - 500 g de harina de trigo<br>
        - 100 g de manteca (o mantequilla)<br>
        - 1 huevo<br>
        - 200 ml de agua<br>
        - 1 cucharadita de sal<br>

        Para el relleno:<br>
        - 400 g de carne picada (puede ser de res, cerdo o mezcla)<br>
        - 1 cebolla grande, picada<br>
        - 1 pimiento rojo o verde, picado (opcional)<br>
        - 2 huevos duros, picados (opcional)<br>
        - 100 g de aceitunas verdes o negras, picadas (opcional)<br>
        - Sal y pimienta al gusto<br>
        - 1 cucharadita de pimentón (dulce o picante, según preferencia)<br>
        - Aceite para cocinar<br>

        Instrucciones<br>

        1. Preparar la masa:<br>
        - En un bol grande, mezcla la harina y la sal.<br>
        - Agrega la manteca derretida y mezcla con las manos hasta que obtengas una textura arenosa.<br>
        - Añade el huevo y el agua poco a poco, amasando hasta que la masa sea suave y homogénea.<br>
        - Cubre la masa con un paño y déjala reposar durante 30 minutos.<br>

        2. Hacer el relleno:<br>
        - En una sartén grande, calienta un poco de aceite y agrega la cebolla y el pimiento. Sofríe hasta que estén
        tiernos.<br>
        - Agrega la carne picada, salpimienta y cocina hasta que esté bien dorada.<br>
        - Incorpora el pimentón y mezcla bien. Si usas huevo duro y aceitunas, agrégalas al final. Retira del fuego y
        deja enfriar.<br>

        3. Armar las empanadas:<br>
        - Precalienta el horno a 200°C (o prepara aceite para freír).<br>
        - Estira la masa sobre una superficie enharinada y corta círculos de aproximadamente 10-12 cm de diámetro.<br>
        - Coloca una cucharada del relleno en el centro de cada círculo.<br>
        - Humedece los bordes con un poco de agua y cierra la empanada, presionando bien para que no se abra.<br>
        - Puedes hacer un repulgue (dobladillo decorativo) si lo deseas.<br>

        4. Cocinar:<br>
        - Si decides hornearlas, colócalas en una bandeja para horno engrasada y píntalas con huevo batido para un
        acabado dorado. Hornea durante 20-25 minutos o hasta que estén doradas.<br>
        - Si prefieres freírlas, calienta suficiente aceite en una sartén y fríe las empanadas hasta que estén doradas
        por ambos lados.<br>

        5. Servir:<br>
        - Deja enfriar un poco antes de servir. ¡Disfruta tus empanadas de carne calientes!<br>

        Consejos<br>
        - Puedes variar el relleno a tu gusto, añadiendo especias o diferentes vegetales.<br>
        - Para congelarlas, colócalas en una bandeja y congélalas antes de cocinarlas. Luego, guárdalas en bolsas
        herméticas y hornéalas o fríelas directamente del congelador.<br>

